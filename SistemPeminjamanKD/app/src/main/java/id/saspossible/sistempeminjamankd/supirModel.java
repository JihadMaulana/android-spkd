public class supirModel {

    private String id_supir;
    private String nama_supir;
    private String no_telepon_supir;
    private String alamat_supir;
    private String ket;

    public String getId_supir() {
        return id_supir;
    }

    public void setId_supir(String id_supir) {
        this.id_supir = id_supir;
    }

    public String getNama_supir() {
        return nama_supir;
    }

    public void setNama_supir(String nama_supir) {
        this.nama_supir = nama_supir;
    }

    public String getNo_telepon_supir() {
        return no_telepon_supir;
    }

    public void setNo_telepon_supir(String no_telepon_supir) {
        this.no_telepon_supir = no_telepon_supir;
    }

    public String getAlamat_supir() {
        return alamat_supir;
    }

    public void setAlamat_supir(String alamat_supir) {
        this.alamat_supir = alamat_supir;
    }

    public String getKet() {
        return ket;
    }

    public void setKet(String ket) {
        this.ket = ket;
    }
}
